    private void botao_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_cadastrarActionPerformed
        // TODO add your handling code here:
        String produto = text_field_cadastrar_produto.getText();
        float preco = Float.parseFloat(text_field_cadastrar_preco.getText());
        Alimento novoAlimento = new Alimento(produto, preco);
        this.cardapio.add(novoAlimento);
    }//GEN-LAST:event_botao_cadastrarActionPerformed

    private void botao_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_atualizarActionPerformed
        String produto = text_field_cadastrar_produto.getText();
        float preco = Float.parseFloat(text_field_cadastrar_preco.getText());
        for (int i = 0; i < cardapio.size(); i++) {
            if (cardapio.get(i).getProduto().equals(produto)) {
                cardapio.get(i).setPreco(preco);
                break;
            }
        }
    }//GEN-LAST:event_botao_atualizarActionPerformed

    private void botao_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_excluirActionPerformed
       String produto = text_field_excluir_produto.getText();
       for (int i = 0; i < cardapio.size(); i++){
           if (cardapio.get(i).getProduto().equals(produto)){
               cardapio.remove(i);
               break;
           }
       }
    }//GEN-LAST:event_botao_excluirActionPerformed

    private void botao_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_buscarActionPerformed
        String produto = text_field_buscar_produto.getText();
        for (int i = 0; i < cardapio.size(); i++){
           if (cardapio.get(i).getProduto().equals(produto)){
               display.setText(cardapio.get(i).toString());
               break;
           }
       }
    }//GEN-LAST:event_botao_buscarActionPerformed

    private void botao_mostrar_cardapioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_mostrar_cardapioActionPerformed
        String completo = "";
        for (Alimento alimento : cardapio) {
            completo =completo + alimento.toString() + "\n";
        }
        display.setText(completo);
    }//GEN-LAST:event_botao_mostrar_cardapioActionPerformed

